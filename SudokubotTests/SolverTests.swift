    func testSolveOneBoard() throws {
        let hints = try XCTUnwrap(Board.deserialize(
            "530070000 600195000 098000060 800060003 400803001 700020006 060000280 000419005 000080079"))
        let solution = try XCTUnwrap(Solver.solve(hints), "this board should be solvable")
        XCTAssertTrue(Solver.verifySolution(solution), "invalid solution")
    }

    func testSolverOverCorpus() throws {
        var verificationCount = 0
        for hintString in Self.corpusHints {
            let hints = try XCTUnwrap(Board.deserialize(hintString))
            if let solution = Solver.solve(hints) {
                XCTAssertTrue(Solver.verifySolution(solution),
                              "solver made a mistake for hints \(hintString)")
                verificationCount += 1
            }
        }
        // every corpus entry, including the blank board, has at least one solution
        XCTAssertEqual(verificationCount, Self.corpusHints.count)
    }

    func testVerifyHints() throws {
        for hintString in Self.corpusHints {
            let hints = try XCTUnwrap(Board.deserialize(hintString))
            XCTAssertTrue(Solver.verifyHints(hints), "hints should be valid: \(hintString)")
        }
    }

    func testSolutionRoundTripsThroughSerialization() throws {
        let hints = try XCTUnwrap(Board.deserialize(Self.corpusHints[0]))
        let solution = try XCTUnwrap(Solver.solve(hints))
        let roundTripped = try XCTUnwrap(Board.deserialize(Board.serialize(solution)))
        XCTAssertEqual(solution, roundTripped)
    }
